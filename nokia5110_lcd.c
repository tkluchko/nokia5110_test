void lcd_send(unsigned char data, char cd)   // Sends data to display controller
{ unsigned char i;

    if(cd == LCD_DATA)                // Êîìàíäà èëè äàííûå
       LCD_DC_PIN = 1;                // Äàííûå
    else   
       LCD_DC_PIN = 0;                // Êîìàíäà

	for(i=0;i<8;i++) 
    {
		if((data>>(7-i)) & 0x01)      // Äàííûå íà ïèí DATA
   		    LCD_DATA_PIN = 1;                 
	    else 
	   	    LCD_DATA_PIN = 0;	                  

   		LCD_CLK_PIN = 1;  
        LCD_CLK_PIN = 0;   
	}
	LCD_DATA_PIN = 1;
    LCD_DC_PIN   = 1;
}
 

